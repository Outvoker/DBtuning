package Merge;

public class Student {
    private int u_id;//学号
    private String u_name;//学生姓名
    private int u_class;//学生班级
    private String u_attend_year;//入学年份
    private String u_native_place;//籍贯
    private String u_birthday;//出生年月
    private String u_major;//专业学院
    private String u_gender;//性别

    public int getU_id(){
        return u_id;
    }
    public void setU_id(int user_id){
        this.u_id = user_id;
    }
    public String getUser_name() {
        return u_name;
    }
    public void setU_name(String user_name) {
        this.u_name = user_name;
    }
    public int getU_class(){
        return u_class;
    }
    public void setU_class(int u_class){
        this.u_class = u_class;
    }
    public String getU_attend_year() {
        return u_attend_year;
    }
    public void setU_attend_year(String u_attend_year) {
        this.u_attend_year = u_attend_year;
    }
    public String getU_native_place() {
        return u_native_place;
    }
    public void setU_native_place(String u_native_place) {
        this.u_native_place = u_native_place;
    }
    public String getU_birthday() {
        return u_birthday;
    }
    public void setU_birthday(String u_birthday) {
        this.u_birthday = u_birthday;
    }
    public String getU_major() {
        return u_major;
    }
    public void setU_major(String u_major) {
        this.u_major = u_major;
    }
    public String getU_gender() {
        return u_gender;
    }
    public void setU_gender(String u_gender) {
        this.u_gender = u_gender;
    }
}
