package Merge;

public class Score {
    private int sco_id;//成绩id
    private int u_id;//学号
    private String sco_year;//学年学期
    private String sco_name;//课程名
    private String sco_property;//属性
    private int sco_period;//学时
    private int sco_credit;//学分
    private  int sco_point;//性别

    public int getSco_id() {
        return sco_id;
    }

    public void setSco_id(int sco_id) {
        this.sco_id = sco_id;
    }

    public int getU_id() {
        return u_id;
    }

    public void setU_id(int u_id) {
        this.u_id = u_id;
    }

    public String getSco_year() {
        return sco_year;
    }

    public void setSco_year(String sco_year) {
        this.sco_year = sco_year;
    }

    public String getSco_name() {
        return sco_name;
    }

    public void setSco_name(String sco_name) {
        this.sco_name = sco_name;
    }

    public String getSco_property() {
        return sco_property;
    }

    public void setSco_property(String sco_property) {
        this.sco_property = sco_property;
    }

    public int getSco_period() {
        return sco_period;
    }

    public void setSco_period(int sco_period) {
        this.sco_period = sco_period;
    }

    public int getSco_credit() {
        return sco_credit;
    }

    public void setSco_credit(int sco_credit) {
        this.sco_credit = sco_credit;
    }

    public int getSco_point() {
        return sco_point;
    }

    public void setSco_point(int sco_point) {
        this.sco_point = sco_point;
    }
}
